#include <algorithm>
class Solution {
public:
    bool isBalanced(TreeNode* root)
    {
        int ans = aux(root);
        if (ans != -1) {
            return true;
        } else {
            return false;
        }
    }

    int aux(TreeNode* node)
    {
        if (node == NULL) {
            return 0;
        }
        int left_h = aux(node->left) + 1;
        if (left_h == 0) {
            return -1;
        }
        int right_h = aux(node->right) + 1;
        if (right_h == 0) {
            return -1;
        }
        return abs(left_h - right_h) > 1 ? -1 : (left_h > right_h ? left_h : right_h);
    }
};
